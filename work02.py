# Operator
num01 = 50
num02 = 99

# +
print("Plus")
plus = num01 + num02
print(plus)

# -
print("Minus")
minus = num01 - num02
print(minus)

# x
print("Multiply")
multiply = num01 * num02
print(multiply)

# /
print("Divide")
divide = num01 / num02
print(divide)

# (**)
print("Complex")
complex = num01 ** num02
print(complex)